// 文字列に含まれる樹種名から分類（'conifer'|'broadleaf'）の集合を返す。substring一致。
export function detectTreeClasses(text) {
  const s = String(text || '');
  const out = new Set();
  if (!s) return out;
  for (const name of CONIFER) if (s.includes(name)) { out.add('conifer'); break; }
  for (const name of BROADLEAF) if (s.includes(name)) { out.add('broadleaf'); break; }
  return out;
}

